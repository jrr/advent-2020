let ``lookup 3`` () =
    lookup (parse input1) (500,500) |> should equal None
    
[<Fact>]
let ``solve 3a example`` () =
    drive (parse input1) (0,0) (3,1) |> should equal 7
    
[<Fact>]
let ``solve 3a problem`` () =
    drive (parse input2) (0,0) (3,1) |> should equal 234
    
let slopes = [(1,1);(3,1);(5,1);(7,1);(1,2)]

[<Fact>]
let ``solve 3b example`` () =
    let map = parse input1
    
    slopes
        |> Seq.map (fun slope -> drive map (0,0) slope)
        |> Seq.reduce (*)

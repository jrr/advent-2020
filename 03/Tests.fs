    parse input |> List.ofSeq |> should equal  [ "..##."; "#...#" ]
    
[<Fact>]
let ``lookup 1`` () =
    lookup (parse input1) (0,0) |> should equal (Some '.')
    
[<Fact>]
let ``lookup 2`` () =
    lookup (parse input1) (11,1) |> should equal (Some '#')
    
